using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace RecursionMethod
{
    internal class SumNaturalNumber
    {
        public void Sum() 
        {
            int sum = 0;

            for (int i = 0; i < 10; i++)
            {
                sum += i;
            }
        }
    }
}
